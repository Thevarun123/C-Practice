#include<iostream>
using namespace std;

int main(){
    
    int a=10, b=20;
    // Arithmetic Operators
    cout<<"Following are the types of operators in c++"<<endl;
    cout<<"The values of a+b: "<<a+b<<endl;
    cout<<"The values of a-b: "<<a-b<<endl;
    cout<<"The values of a*b: "<<a*b<<endl;
    cout<<"The values of a/b: "<<a/b<<endl;
    cout<<"The values of a%b: "<<a%b<<endl;
    cout<<"The values of a++: "<<a++<<endl;
    cout<<"The values of a--: "<<a--<<endl;
    cout<<"The values of ++a: "<<++a<<endl;
    cout<<"The values of --a: "<<--a<<endl;
    cout<<endl;


     // Comparison Operators
    cout<<"The following are examples of comparison operators"<<endl;
    cout<<"The value of a==b: "<<(a==b)<<endl;
    cout<<"The value of a<b:  "<<(a<b)<<endl;
    cout<<"The value of a>b:  "<<(a>b)<<endl;
    cout<<"The value of a<=b: "<<(a<=b)<<endl;
    cout<<"The value of a>=b: "<<(a>=b)<<endl;
    cout<<endl;

    // Logical Operators
    cout<<"The following are the logical operators:"<<endl;
    cout<<"The value of (a==b) && (a<b):"<<((a==b) && (a<b))<<endl;    
    cout<<"The value of (a==b) || (a<b):"<<((a==b) || (a<b))<<endl;    
    cout<<"The value of !(a==b): "<<!(a==b)<<endl;    
        


    return 0;

}